package es.altia.flexia.integracion.moduloexterno.meikus.pasarelapago.vo;


public class PagoPasarelaVO {
    
    private String numExpediente;
    private Integer codOrganizacion;
    private String ejercicio;
    private String numPago;
    private String codProcedimiento;
    private String idExpedientePasarela;
    
    public String getNumExpediente() {
        return numExpediente;
    }//getNumExpediente
    public void setNumExpediente(String numExpediente) {
        this.numExpediente = numExpediente;
    }//setNumExpediente
    
    public Integer getCodOrganizacion() {
        return codOrganizacion;
    }//getCodOrganizacion
    public void setCodOrganizacion(Integer codOrganizacion) {
        this.codOrganizacion = codOrganizacion;
    }//setCodOrganizacion
    
    public String getEjercicio() {
        return ejercicio;
    }//getEjercicio
    public void setEjercicio(String ejercicio) {
        this.ejercicio = ejercicio;
    }//setEjercicio
    
    public String getNumPago() {
        return numPago;
    }//getNumPago
    public void setNumPago(String numPago) {
        this.numPago = numPago;
    }//setNumPago
    
    public String getCodProcedimiento() {
        return codProcedimiento;
    }//getCodProcedimiento
    public void setCodProcedimiento(String codProcedimiento) {
        this.codProcedimiento = codProcedimiento;
    }//setCodProcedimiento

    public String getIdExpedientePasarela() {
        return idExpedientePasarela;
    }//getIdExpedientePasarela
    public void setIdExpedientePasarela(String idExpedientePasarela) {
        this.idExpedientePasarela = idExpedientePasarela;
    }//setIdExpedientePasarela

}//class
