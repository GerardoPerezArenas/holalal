package es.altia.flexia.integracion.moduloexterno.melanbide72.vo;

/**
 *
 * @author jignacio
 */
public class MedidaA1BCVO {
    
    private Integer id;
    private String numExp;
    private String nombre;
    private String nif;
    private Double importeAnual;
    private String objetoContrato;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNumExp() {
        return numExp;
    }

    public void setNumExp(String numExp) {
        this.numExp = numExp;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNif() {
        return nif;
    }

    public void setNif(String nif) {
        this.nif = nif;
    }

    public Double getImporteAnual() {
        return importeAnual;
    }

    public void setImporteAnual(Double importeAnual) {
        this.importeAnual = importeAnual;
    }

    public String getObjetoContrato() {
        return objetoContrato;
    }

    public void setObjetoContrato(String objetoContrato) {
        this.objetoContrato = objetoContrato;
    }
    
    
    
}
