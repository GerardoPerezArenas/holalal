public class JustificacionECA23VO {

    private String numExpediente;
    private Double importeEca;
    private Double importeValidado;
    private Double totalInserciones;
    private Double totalSeguimientos;

    public String getNumExpediente() {
        return numExpediente;
    }

    public void setNumExpediente(String numExpediente) {
        this.numExpediente = numExpediente;
    }

    public Double getImporteEca() {
        return importeEca;
    }

    public void setImporteEca(Double importeEca) {
        this.importeEca = importeEca;
    }

    public Double getImporteValidado() {
        return importeValidado;
    }

    public void setImporteValidado(Double importeValidado) {
        this.importeValidado = importeValidado;
    }

    public Double getTotalInserciones() {
        return totalInserciones;
    }

    public void setTotalInserciones(Double totalInserciones) {
        this.totalInserciones = totalInserciones;
    }

    public Double getTotalSeguimientos() {
        return totalSeguimientos;
    }

    public void setTotalSeguimientos(Double totalSeguimientos) {
        this.totalSeguimientos = totalSeguimientos;
    }

}
