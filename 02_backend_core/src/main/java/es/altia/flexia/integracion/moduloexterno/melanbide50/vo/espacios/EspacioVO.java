package es.altia.flexia.integracion.moduloexterno.melanbide50.vo.espacios;

/**
 *
 * @author kepa
 */
public class EspacioVO {

    private Integer id;
    private String numExp;
    private Integer idEspSol;
    private String denominacion;
    private Integer numAlumnos;
    private String espAcred;
    private String espAutor;
    private Integer alumNuevos;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNumExp() {
        return numExp;
    }

    public void setNumExp(String numExp) {
        this.numExp = numExp;
    }

    public Integer getIdEspSol() {
        return idEspSol;
    }

    public void setIdEspSol(Integer idEspSol) {
        this.idEspSol = idEspSol;
    }

    public String getDenominacion() {
        return denominacion;
    }

    public void setDenominacion(String denominacion) {
        this.denominacion = denominacion;
    }

    public Integer getNumAlumnos() {
        return numAlumnos;
    }

    public void setNumAlumnos(Integer numAlumnos) {
        this.numAlumnos = numAlumnos;
    }

    public String getEspAcred() {
        return espAcred;
    }

    public void setEspAcred(String espAcred) {
        this.espAcred = espAcred;
    }

    public String getEspAutor() {
        return espAutor;
    }

    public void setEspAutor(String espAutor) {
        this.espAutor = espAutor;
    }

    public Integer getAlumNuevos() {
        return alumNuevos;
    }

    public void setAlumNuevos(Integer alumNuevos) {
        this.alumNuevos = alumNuevos;
    }

}
