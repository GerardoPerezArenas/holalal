package es.altia.flexia.integracion.moduloexterno.melanbide47.vo.comun;

/**
 *
 * @author santiagoc
 */
public class ProvinciaVO 
{
    private Integer prvCod;
    private Integer prvPai;
    private Integer prvAut;
    private String prvNom;
    private String prvNol;
    
    public ProvinciaVO()
    {
        
    }

    public Integer getPrvCod() {
        return prvCod;
    }

    public void setPrvCod(Integer prvCod) {
        this.prvCod = prvCod;
    }

    public Integer getPrvPai() {
        return prvPai;
    }

    public void setPrvPai(Integer prvPai) {
        this.prvPai = prvPai;
    }

    public Integer getPrvAut() {
        return prvAut;
    }

    public void setPrvAut(Integer prvAut) {
        this.prvAut = prvAut;
    }

    public String getPrvNom() {
        return prvNom;
    }

    public void setPrvNom(String prvNom) {
        this.prvNom = prvNom;
    }

    public String getPrvNol() {
        return prvNol;
    }

    public void setPrvNol(String prvNol) {
        this.prvNol = prvNol;
    }
}
