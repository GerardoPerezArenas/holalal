public class JusPreparadoresECA23VO {

    private Integer id;
    private String numExpediente;
    private String nifPreparador;
    private Double jornada;
    private Integer permitidos;
    private Integer segumientos;
    private Double importePrep;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNumExpediente() {
        return numExpediente;
    }

    public void setNumExpediente(String numExpediente) {
        this.numExpediente = numExpediente;
    }

    public String getNifPreparador() {
        return nifPreparador;
    }

    public void setNifPreparador(String nifPreparador) {
        this.nifPreparador = nifPreparador;
    }

    public Double getJornada() {
        return jornada;
    }

    public void setJornada(Double jornada) {
        this.jornada = jornada;
    }

    public Integer getPermitidos() {
        return permitidos;
    }

    public void setPermitidos(Integer permitidos) {
        this.permitidos = permitidos;
    }

    public Integer getSegumientos() {
        return segumientos;
    }

    public void setSegumientos(Integer segumientos) {
        this.segumientos = segumientos;
    }

    public Double getImportePrep() {
        return importePrep;
    }

    public void setImportePrep(Double importePrep) {
        this.importePrep = importePrep;
    }

}
