package es.altia.flexia.integracion.moduloexterno.lanbide01.vo;

public class AlarmaVO {
    private String codigoAlarma;
    private int diasExcedidos;

    /**
     * @return the codigoAlarma
     */
    public String getCodigoAlarma() {
        return codigoAlarma;
    }

    /**
     * @param codigoAlarma the codigoAlarma to set
     */
    public void setCodigoAlarma(String codigoAlarma) {
        this.codigoAlarma = codigoAlarma;
    }

    /**
     * @return the diasExcedidos
     */
    public int getDiasExcedidos() {
        return diasExcedidos;
    }

    /**
     * @param diasExcedidos the diasExcedidos to set
     */
    public void setDiasExcedidos(int diasExcedidos) {
        this.diasExcedidos = diasExcedidos;
    }    
     
}