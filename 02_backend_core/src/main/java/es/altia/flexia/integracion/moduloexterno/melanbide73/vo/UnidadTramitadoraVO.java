/**
 *
 * @author Kepa
 */
public class UnidadTramitadoraVO {
    
    //Propiedades
    private String codUnidad;
    private String nomUnidad;
    private String codVisible;
    private String numExpediente;
    // getters y setters

    public String getCodUnidad() {
        return codUnidad;
    }

    public void setCodUnidad(String codUnidad) {
        this.codUnidad = codUnidad;
    }

    public String getNomUnidad() {
        return nomUnidad;
    }

    public void setNomUnidad(String nomUnidad) {
        this.nomUnidad = nomUnidad;
    }

    public String getCodVisible() {
        return codVisible;
    }

    public void setCodVisible(String codVisible) {
        this.codVisible = codVisible;
    }

    public String getNumExpediente() {
        return numExpediente;
    }

    public void setNumExpediente(String numExpediente) {
        this.numExpediente = numExpediente;
    }
    
    
}
