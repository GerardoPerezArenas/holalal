/**
 *
 * @author kepa
 */
public class RegistroAerteVO {

    private Integer resDep;
    private Integer resUor;
    private String resTip;
    private Integer resEje;
    private Integer resNum; 
    private Integer resUsu;
    private Integer resTer;
    private String resAsu;  
    private Integer numSolicitud;
    
    public RegistroAerteVO() {
    }

    public Integer getResDep() {
        return resDep;
    }

    public void setResDep(Integer resDep) {
        this.resDep = resDep;
    }

    public Integer getResUor() {
        return resUor;
    }

    public void setResUor(Integer resUor) {
        this.resUor = resUor;
    }

    public String getResTip() {
        return resTip;
    }

    public void setResTip(String resTip) {
        this.resTip = resTip;
    }

    public Integer getResEje() {
        return resEje;
    }

    public void setResEje(Integer resEje) {
        this.resEje = resEje;
    }

    public Integer getResNum() {
        return resNum;
    }

    public void setResNum(Integer resNum) {
        this.resNum = resNum;
    }

    public Integer getResUsu() {
        return resUsu;
    }

    public void setResUsu(Integer resUsu) {
        this.resUsu = resUsu;
    }

    public Integer getResTer() {
        return resTer;
    }

    public void setResTer(Integer resTer) {
        this.resTer = resTer;
    }

    public String getResAsu() {
        return resAsu;
    }

    public void setResAsu(String resAsu) {
        this.resAsu = resAsu;
    }

    public Integer getNumSolicitud() {
        return numSolicitud;
    }

    public void setNumSolicitud(Integer numSolicitud) {
        this.numSolicitud = numSolicitud;
    }
    
}
